
-- prizes
CREATE TABLE prizes (
  id INTEGER PRIMARY KEY ASC NOT NULL,
  star_val INT NOT NULL,
  goal INT NOT NULL,
  first_dow INT NOT NULL,
  start STRING,
  end STRING
);

-- stars
CREATE TABLE stars (
  id INTEGER PRIMARY KEY ASC NOT NULL,
  at STRING NOT NULL,
  typ INT NOT NULL,
  prize_id INTEGER NOT NULL,
  got BOOL
);

-- wins
CREATE TABLE wins (
  id INTEGER PRIMARY KEY ASC NOT NULL,
  at STRING NOT NULL,
  prize_id INTEGER NOT NULL,
  got BOOL NOT NULL,
);

-- deposits
CREATE TABLE deposits (
  id INTEGER PRIMARY KEY ASC NOT NULL,
  at STRING NOT NULL,
  amount INT NOT NULL,
  prize_id INTEGER NOT NULL,
  desc STRING NOT NULL
);

-- users
CREATE TABLE users {
  id INTEGER PRIMARY KEY ASC NOT NULL,
  name STRING NOT NULL,
  psk STRING NOT NULL,
  perms INTEGER NOT NULL,
}

